function y = myfiltroFIRes3(b,x)


y = zeros(size(x));
y(1) = b(1)*x(1);
y(2) = b(2)*x(1)+b(1)*x(2);
y(3) = b(3)*x(1)+b(2)*x(2)+b(1)*x(3);
y(4) = b(4)*x(1)+b(3)*x(2)+b(2)*x(3)+b(1)*x(4);
y(5) = b(5)*x(1)+b(4)*x(2)+b(3)*x(3)+b(2)*x(4)+b(1)*x(5);
y(6) = b(6)*x(1)+b(5)*x(2)+b(4)*x(3)+b(3)*x(4)+b(2)*x(5)...
    +b(1)*x(6);
y(7) = b(7)*x(1)+b(6)*x(2)+b(5)*x(3)+b(4)*x(4)+b(3)*x(5)...
    +b(2)*x(6)+b(1)*x(7);
y(8) = b(8)*x(1)+b(7)*x(2)+b(6)*x(3)+b(5)*x(4)+b(4)*x(5)...
    +b(3)*x(6)+b(2)*x(7)+b(1)*x(8);
y(9) = b(9)*x(1)+b(8)*x(2)+b(7)*x(3)+b(6)*x(4)+b(5)*x(5)...
    +b(4)*x(6)+b(3)*x(7)+b(2)*x(8)+b(1)*x(9); 
for n = 10:length(x)
    y(n) = b(10)*x(n)+b(9)*x(n-1)+b(8)*x(n-2)+b(7)*x(n-3)...
        +b(6)*x(n-4)+b(5)*x(n-5)+b(4)*x(n-6)+b(3)*x(n-7)...
        +b(2)*x(n-8)+b(1)*x(n-9); 
end